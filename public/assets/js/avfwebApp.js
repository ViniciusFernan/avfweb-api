let avfweb = {
    // var
    url: '/',
    urlApi: '/api',

    //functions
    alert: function(title, mensagem, tipo) {
        if(!title) {
            title = tipo==1 ? "Sucesso" : "Error!";
        }

        Swal.fire({
            title: title,
            text: mensagem,
            icon: tipo==1 ? "success" : tipo==3 ? "question" : "error",
            confirmButtonText: 'Fechar'
        })
    },

    loading: function() {
        var loadingHtml = '<div class="spinner-box"><div class="spinner-border" role="status"><span class="visually-hidden">Loading...</span></div></div>'

        if (typeof($("div.spinner-box")) !== "undefined"  && $("div.spinner-box").length==0 ) {
            $('body').append(loadingHtml);
        } else {
            $('.spinner-box').fadeOut(800, function () {
                $(this).remove();
            }).remove();
        }
    },

    redirectUrl: function (page) {
        if(page){
            window.location.href = "/" + page;
        }
    }
}

